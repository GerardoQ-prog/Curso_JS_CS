//MATH 


//Math.max

const numero1 = 100

const numero2 = 10

const resultado = Math.max(numero1,numero2)

console.log('max', resultado)




//Math.min

const resultado1 = Math.min(numero1, numero2)

console.log('min',resultado1)




//Math.random

const resultado2 = Math.random()*10 + 1

console.log('random',
parseFloat(resultado2.toFixed(4)))




//Math.round

const resultado3 = Math.round(resultado2)

console.log('round',resultado3)



//Math.floor

const resultado4 = Math.floor(resultado2)
console.log('floor',resultado4)



//Math.ceil

const resultado5 = Math.ceil(resultado2)
console.log('ceil', resultado5)



//Math.PI

console.log('pi',Math.PI)






